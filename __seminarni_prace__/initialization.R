## instaluji a inicializuji balíčky -------------------------------------------

for(my_package in c(
    
    "openxlsx",
    "xtable",
    "data.table",
    #"forecast",
    #"astsa",
    #"vars",
    "lme4"
    
)){
    
    if(!(my_package %in% rownames(installed.packages()))){
        
        install.packages(
            my_package,
            dependencies = TRUE,
            repos = "http://cran.us.r-project.org"
        )
        
    }
    
    library(my_package, character.only = TRUE)
    
}


